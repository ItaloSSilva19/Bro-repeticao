print('\nCalcule a soma e o dobro da soma de dois números reais.')

#def. variável

num_1 = float(input('\nDigite o 1º número: '))
num_2 = float(input('Digite o 2º número: '))

#calcular soma
soma = num_1 + num_2

#calcular 2 * soma

dobro = 2 * soma

# retornar valores calculados

print('A soma dos números é: ', soma)
print('O dobro da soma dos números é: ', dobro)
